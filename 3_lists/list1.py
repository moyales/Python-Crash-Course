fruits = ["apples", "oranges", "bananas", "peaches"]
#Displays the entire list of fruits, with square brackets and quotes
print(fruits)

#Displays only that part of this list, starting from position 0
print(fruits[2])

#Capitalise the word with ".title()"
print(fruits[2].title())

#Will print the last item of the list. Higher negative integers go further down the list, but backwards
print(fruits[-1])
