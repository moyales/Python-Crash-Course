#Exercise 4.10: Slices
#Make slices in a list.
cities = ['new york', 'los angeles', 'london', 'tokyo', 'sydney', 'paris', 'mumbai', 'chicago', 'montreal']

print("The first three items in this list are:")
print(cities[:3])

print("\nThree items from this middle of the list are:")
print(cities[3:6])

print("\nThe last three items in the list are:")
print(cities[6:])
